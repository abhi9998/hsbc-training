public class Mouse {

	private int price;
	private boolean isWired;
	
	public Mouse(int price, boolean isWired) {
	
		this.price = price;
		this.isWired = isWired;
	}

	public int getPrice() {
		return price;
	}

	public boolean isWired() {
		return isWired;
	}
	
	
	
	
}
